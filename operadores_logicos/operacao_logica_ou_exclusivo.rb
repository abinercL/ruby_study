#A operação lógica ‘ou exclusivo’, também conhecida como xor (do inglês exclusive or)
#retorna verdadeiro quando apenas um dos operadores é verdadeiro.
#Em todos os outros casos, será retornado falso.

#exemplo codigo

puts "true ^ true = #{true ^ true}"
puts "true ^ false = #{true ^ false}"
puts "false ^ true = #{false ^ true}"
puts "false ^ true = #{false ^ true}"
